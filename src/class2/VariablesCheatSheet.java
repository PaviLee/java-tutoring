package class2;

public class VariablesCheatSheet {

	public static void main(String[] args) {
		System.out.println(10); // default: int
		System.out.println(1.0); // default: double

		// Primitive Data Types
		int wholeNumber = -10;
		double decimal = -0.1;

		char character = ';';

		boolean bool = true; // true or false

		// Non-Primitive Data Types
		String text = "School starts in two days!";
	}

}

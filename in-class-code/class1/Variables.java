package class1;

public class Variables {

	public static void main(String[] args) {
		// dataType variableName = variableValue;
		int x = 10;

		// 8 Primitive Data Type
		boolean b = true;

		// Character
		char c = 'd';

		// Numbers: Integers
		byte num1 = 10;
		short num2 = 20;
		int num3 = 30;
		long num4 = 40;

		// Numbers: decimals
		float decimal1 = 1.1111111111111111111111111f;
		double decimal2 = 1.1111111111111111111111111;

		System.out.println(decimal1); // less accurate
		System.out.println(decimal2); // more accurate

		// Non-Primitve
		String str = "Name";
	}
}